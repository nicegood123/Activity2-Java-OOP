    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jTextArea1.setText("");
        hold = "Result:\n";
        for (int i = 0; i < name.size(); i++) {
            hold += name.get(i) + "\n";
        }
        jTextArea1.setText(hold);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        hold = "Result:\n";
        if (name.contains(jTextField1.getText())) {
            JOptionPane.showMessageDialog(null, "DATA FOUND: " + jTextField1.getText());
            hold += name.get(name.indexOf(jTextField1.getText()));
        } else {
            hold = "NO DATA FOUND...";
            JOptionPane.showMessageDialog(null, hold);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

